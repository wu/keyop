// ── trimSpace ─────────────────────────────────────────────────────────────────

func TestTrimSpace(t *testing.T) {
	cases := []struct{ in, want string }{
		{"hello", "hello"},
		{"  hello  ", "hello"},
		{"hello   world", "hello world"},
		{"\thello\n", "hello"},
		{"", ""},
		{"   ", ""},
	}
	for _, c := range cases {
		if got := trimSpace(c.in); got != c.want {
			t.Errorf("trimSpace(%q) = %q, want %q", c.in, got, c.want)
		}
	}
}

