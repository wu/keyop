func TestListMovies_SortByTitle_IgnoresThe(t *testing.T) {
	db := openTestDB(t)
	mustInsert(t, db, movie{Title: "The Mummy", Year: 1999})
	mustInsert(t, db, movie{Title: "Alien", Year: 1979})
	mustInsert(t, db, movie{Title: "Batman", Year: 1989})

	results, _ := listMovies(db, "", "", "", "title", false)
	titles := make([]string, len(results))
	for i, m := range results {
		titles[i] = m.Title
	}
	// "The Mummy" should sort as "Mummy" → between Batman and... after Batman
	want := []string{"Alien", "Batman", "The Mummy"}
	for i, w := range want {
		if i >= len(titles) || titles[i] != w {
			t.Errorf("sort by title: want %v, got %v", want, titles)
			break
		}
	}
}

