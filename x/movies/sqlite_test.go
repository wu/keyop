// openTestDB creates a temporary SQLite database with the movies schema applied.
func openTestDB(t *testing.T) *sql.DB {
	t.Helper()
	dir := t.TempDir()
	db, err := sql.Open("sqlite", filepath.Join(dir, "movies_test.db"))
	if err != nil {
		t.Fatalf("open test db: %v", err)
	}
	if _, err := db.Exec(moviesSchema); err != nil {
		t.Fatalf("create schema: %v", err)
	}
	t.Cleanup(func() { _ = db.Close() })
	return db
}

